    public static boolean isShootAction(int action) {
        return action >= SHOOT1 && action <= SHOOTF
                || action == AVENGER
                || action == SHOT
                || action == FAKE
                || action == HANDGUN
                || action == DOUBLEFIRE
                || action == TRIPLEFIRE
                || action == RAPIDFIRE
                || action == BACKSTEP
                || action == HOMING
                || action == STORM_BREAK
                || action == WIND_SPEAR
                || action == WIND_SHOT
                || action == MONSTER_BOMB_PREPARE
                || action == FINAL_CUT_PREPARE;
    }

    public static boolean isProneStabAction(int action) { return action == PRONESTAB || action == PRONESTAB_JAGUAR; }

